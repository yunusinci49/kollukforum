        <summary><span class="name">Tek Taraflı Yaralanmalı ve Maddi Hasarlı Trafik Kazası</span> <span class="lang">[2]</span></summary><br>
       <code class="code scss">

        <div class="metin">

  <ol>
  <li>Trafik kazasında yaralı var ve adli rapor düzenlenmiş ise adli tahkikata başlanır.</li>
  <li>Kazanın gerçekleştiği yerin olay yeri emniyeti alınır. (<strong><font color="#FF6161"> ·</font></strong> Çift şeritli yolsa karşı şeridin geçisi kontrollü geçiş sağlanır, <strong><font color="#FF6161"> ·</font></strong> Akşam vaktiyse ışıklı dubalar ile olay yeri görünür hale getirilir,  <strong><font color="#FF6161"> ·</font></strong> Diğer araçlar bizi görmesi için reflektörlü yelekler giyilir,  <strong><font color="#FF6161"> ·</font></strong> Ölü veya yaralı varsa kimsenin yaklaşmaması sağlanır.)</li>
  <li>Hızlı bir şekilde araç şöförleri belirlenir. (Buradaki maksat daha sonra şahıslar ehliyetsiz veya alkollü olduğu için şöför olduğunu inkar edebiliyor. Eğer gerçekten tarafımızca sürücü olduğunu bildiğimiz kişi sürücü olduğunu inkar ediyor ise savcı talimatı alınarak Olay Yeri İnceleme ekibi çağırıp direksiyon üzerinde parmak izi incelemesi yaptırılabilir.)</li>
  <li>Araç sürücüsü ve yolcuların alkol testi yapılır, sürücü hastaneye kaldırılmış ise hastanede kan değerlerinden alkol tespiti istenir. (Promil değerleri mutlaka yazılmalı)</li>
  <li>Tarafların ifadeleri yazılır. (Sürücüler birbirinden şikayetçi ise Müşteki-Şüpheli değiller ise Şüpheli sıfatı ile, araç içindekiler tanık sıfatı ile)</li>
  <li>Olay yerini gören kamera varsa kayıtları alınır</li>
  <li>Olay yeri fotoğraflaması yapılır.</li>
  <li>Olay yeri görgü tespit tutanağının tanzim edilmesi. (Kaza yapılan yerin kaç şeritli olduğu, meskun mahal içi/dışı mı olduğu, akşam ise aydınlatmanın olup olmadığı, yerlerin kaygan olup olmadığı, araçların yolun neresinde durmuş oldukları, sürüklenmenin olup olmadığı, asfalt mı beton mu toprak mı olduğu, trafik işaretleri var ise neler olduğu, sollama tabelasının olup olmadığı, havanın sisli, karlı, yağmurlu vs. olup olmadığı, kazanın saat kaçta gerçekleştiği, )</li>
  <li>Araç ve içindeki eşyalar ile birlikte şahıslara tutanak ile teslim edilir. (Kazazedeler alamayacak durumda ise yakınlarına da teslim edilebilir.)</li>
  <li>Ölümlü bir kaza ise savcı adli tıp incelemesi ile ölü muayenesi yaptırabilir.</li>
  <li>Trafik ekiplerinin tanzim ettiği Trafik Kazası Raporu Tahkikata eklenir.</li>
  <li>Bilgi Notu ve Vukuat Raporu Çekilir(Teşkilat İçi)</li>
  <li>Hazırlanan Tahkikat evrakları ikmalen gönderilir.</li>
</ol>

